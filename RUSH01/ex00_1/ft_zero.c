void	ft_zero(int table[4][4])
{
	int	row;
	int	col;

	row = 0;
	col = 0;
	while (row < 4)
	{
		while (col < 4)
		{
			table[row][col] = 0;
			col++;
		}
		col = 0;
		row++;
	}
}
